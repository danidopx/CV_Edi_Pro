CREATE OR REPLACE FUNCTION public.desativar_minha_conta()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  UPDATE auth.users
  SET email = id::text || '_desativado@inativo.local',
      encrypted_password = 'DISABLED-' || gen_random_uuid()::text
  WHERE id = auth.uid();
END;
$$;
